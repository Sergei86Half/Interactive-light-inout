package main

import (
	"fmt"
	"golang.org/x/term" // Для работы с терминалом в "сыром" режиме
	"os"
	"os/signal"
	"strings"
	"syscall"
)

// clearScreen очищает экран терминала и перемещает курсор в начало.
func clearScreen() {
	// Проверяем, является ли stdout терминалом, прежде чем пытаться очистить
	if term.IsTerminal(int(os.Stdout.Fd())) {
		// \033[2J - очистить весь экран
		// \033[H - переместить курсор в домашнюю позицию (верхний левый угол)
		fmt.Print("\033[2J\033[H")
	}
}

// getChar читает один символ из stdin без ожидания Enter и без эха.
func getChar() (string, error) {
	fd := int(os.Stdin.Fd())

	// !!! ДОБАВЛЕНА ПРОВЕРКА !!!
	if !term.IsTerminal(fd) {
		// Если stdin не является терминалом, мы не можем работать в сыром режиме.
		// Возвращаем ошибку, которая будет обработана в main.
		return "", fmt.Errorf("stdin не является терминалом! \nЗапустите программу в интерактивном терминале!(типа notepad методом копирования,сохранения кода в формате .go)")
	}

	// Сохраняем текущее состояние терминала и гарантируем его восстановление при выходе.
	oldState, err := term.MakeRaw(fd)
	if err != nil {
		return "", fmt.Errorf("не удалось перевести терминал в сырой режим: %w", err)
	}
	defer term.Restore(fd, oldState) // Это очень важно!

	buf := make([]byte, 1) // Буфер для одного символа
	n, err := os.Stdin.Read(buf)
	if err != nil {
		return "", fmt.Errorf("не удалось прочитать из stdin: %w", err)
	}
	return string(buf[:n]), nil
}

func main() {
	// Устанавливаем обработчик для Ctrl+C, чтобы программа завершалась чисто.
	sigChan := make(chan os.Signal, 1)
	signal.Notify(sigChan, os.Interrupt, syscall.SIGTERM)

	// Запускаем горутину, которая будет слушать сигналы
	go func() {
		<-sigChan // Ждем сигнала
		// Очищаем строку перед выводом сообщения о выходе
		// Используем достаточное количество пробелов, чтобы гарантированно очистить строку статуса
		fmt.Printf("\r%s\rПрограмма прервана пользователем (Ctrl+C).\n", strings.Repeat(" ", 80))
		os.Exit(0)
	}()

	clearScreen() // Очищаем терминал при запуске - делаем его пустым

	// Определяем максимальную длину для сообщений, чтобы корректно очищать строку
	// "Светильник выключен" (19 символов) длиннее, чем "Светильник включен" (17 символов)
	maxLenMessage := len("Светильник выключен")

	lightOn := false // Начальное логическое состояние: светильник выключен. Ничего пока не отображается.

	for {
		char, err := getChar() // Ждем нажатия клавиши
		if err != nil {
			// Обработка ошибки, если терминал не найден
			// Очищаем строку перед выводом сообщения об ошибке
			fmt.Printf("\r%s\rОшибка: %v\n", strings.Repeat(" ", maxLenMessage+10), err) // +10 для запаса
			// Дополнительное сообщение для пользователя
			fmt.Println("Запустите программу в стандартном командном окне (cmd.exe, PowerShell, Git Bash и т.п.).")
			break // Выходим из цикла, так как интерактивный режим невозможен
		}

		// Проверяем, является ли нажатая клавиша Esc (ASCII 27 или '\x1b')
		if char == "\x1b" {
			// Очищаем текущую строку перед выходом
			fmt.Printf("\r%s\rВыход из программы.\n", strings.Repeat(" ", maxLenMessage+10)) // +10 для запаса
			break // Выходим из бесконечного цикла
		}

		// Переключаем состояние светильника только после нажатия клавиши
		lightOn = !lightOn

		var statusMessage string
		if lightOn {
			statusMessage = "Светильник включен"
		} else {
			statusMessage = "Светильник выключен"
		}

		// Используем '\r' для возврата курсора в начало строки
		// и пробелы для очистки предыдущего текста, если новый короче.
		// Это гарантирует, что на экране всегда будет только текущее сообщение.
		fmt.Printf("\r%s%s", statusMessage, strings.Repeat(" ", maxLenMessage-len(statusMessage)))
	}
}

